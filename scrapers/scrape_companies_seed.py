def _c(
    name, name_ar="", country="", country_code="", city="",
    sectors=None, company_type="contractor", employee_count=None,
    annual_revenue=None, revenue_currency="USD", founded_year=None,
    website="", certifications=None, notable_projects=None,
    jv_experience=False, international_presence=None, tags=None,
    description="", subsectors=None, prequalified_with=None,
):
    """Shorthand builder for a seed company record."""
    sectors = sectors or ["construction"]
    certifications = certifications or []
    notable_projects = notable_projects or []
    international_presence = international_presence or [country_code]
    tags = tags or []
    subsectors = subsectors or []
    prequalified_with = prequalified_with or []

    ref = f"seed-{country_code}-{name}"
    return {
        "id": generate_company_id("seed", ref),
        "name": name,
        "name_ar": name_ar,
        "name_fr": "",
        "legal_name": name,
        "source": "seed",
        "source_ref": ref,
        "source_url": website or "",
        "country": country,
        "country_code": country_code,
        "city": city,
        "address": "",
        "website": website,
        "email": "",
        "phone": "",
        "sector": sectors[0] if sectors else "construction",
        "sectors": sectors,
        "subsectors": subsectors,
        "company_type": company_type,
        "company_size": classify_company_size(employee_count, annual_revenue),
        "employee_count": employee_count,
        "annual_revenue": annual_revenue,
        "revenue_currency": revenue_currency,
        "founded_year": founded_year,
        "registration_number": "",
        "certifications": certifications,
        "classifications": [],
        "prequalified_with": prequalified_with,
        "notable_projects": notable_projects,
        "jv_experience": jv_experience,
        "international_presence": international_presence,
        "description": description,
        "description_ar": "",
        "description_fr": "",
        "tags": ["seed", "curated"] + tags,
        "metadata": {"data_source": "manual_curation"},
        "verified": True,
        "active": True,
    }


